
package com.backend.estadisticas.model;

public class Estadisticas {
    
  public String codSegmento;
  public String desSegmento;
  public String tipoArticulo;
  public String desTipoArticulo;

    public String getCodSegmento() {
        return codSegmento;
    }

    public void setCodSegmento(String codSegmento) {
        this.codSegmento = codSegmento;
    }

    public String getDesSegmento() {
        return desSegmento;
    }

    public void setDesSegmento(String desSegmento) {
        this.desSegmento = desSegmento;
    }

    public String getTipoArticulo() {
        return tipoArticulo;
    }

    public void setTipoArticulo(String tipoArticulo) {
        this.tipoArticulo = tipoArticulo;
    }

    public String getDesTipoArticulo() {
        return desTipoArticulo;
    }

    public void setDesArticulo(String desTipoArticulo) {
        this.desTipoArticulo = desTipoArticulo;
    }
    
}
